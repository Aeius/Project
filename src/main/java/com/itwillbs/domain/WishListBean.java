package com.itwillbs.domain;

public class WishListBean {
	
	
	private String wishList_member_email;
	
	private String wishList_product_idx;
	
	private String product_name;
	
	private String product_price;
	
	private String product_stock;
	
	
	public String getWishList_member_email() {
		return wishList_member_email;
	}
	public void setWishList_member_email(String wishList_member_email) {
		this.wishList_member_email = wishList_member_email;
	}
	public String getWishList_product_idx() {
		return wishList_product_idx;
	}
	public void setWishList_product_idx(String wishList_product_idx) {
		this.wishList_product_idx = wishList_product_idx;
	}
	public String getProduct_name() {
		return product_name;
	}
	public void setProduct_name(String product_name) {
		this.product_name = product_name;
	}
	public String getProduct_price() {
		return product_price;
	}
	public void setProduct_price(String product_price) {
		this.product_price = product_price;
	}
	public String getProduct_stock() {
		return product_stock;
	}
	public void setProduct_stock(String product_stock) {
		this.product_stock = product_stock;
	}
	
	
	
}
