package com.itwillbs.domain;

public class RecoBean {
	
	private String category;
	private int man;
	private int woman;
	private int non;
	
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public int getMan() {
		return man;
	}
	public void setMan(int man) {
		this.man = man;
	}
	public int getWoman() {
		return woman;
	}
	public void setWoman(int woman) {
		this.woman = woman;
	}
	public int getNon() {
		return non;
	}
	public void setNon(int non) {
		this.non = non;
	}
	
}
